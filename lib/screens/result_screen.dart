class ResultScreen extends StatefulWidget {
  const ResultScreen({super.key});

  static const routeName = '/result';

  @override
  State<ResultScreen> createState() => _ResultScreenState();
}

class _ResultScreenState extends State<ResultScreen> {
  final HistoryService _historyService = HistoryService();
  final PdfService _pdfService = PdfService();
  bool _savedToHistory = false;
  bool _isExporting = false;
  bool _diseaseInfoExpanded = false;

  @override
  void didChangeDependencies() {
    super.didChangeDependencies();
    _saveToHistory();
  }

  Future<void> _saveToHistory() async {
    if (_savedToHistory) return;
    
    final predictionProvider = context.read<PredictionProvider>();
    final args = ModalRoute.of(context)?.settings.arguments as Map<String, dynamic>?;
    
    if (args != null && predictionProvider.selectedImage != null) {
      final record = ScanRecord.fromPrediction(
        result: args,
        imagePath: predictionProvider.selectedImage!.path,
      );
      await _historyService.saveRecord(record);
      _savedToHistory = true;
    }
  }

  Future<void> _exportPdf() async {
    final predictionProvider = context.read<PredictionProvider>();
    final args = ModalRoute.of(context)?.settings.arguments as Map<String, dynamic>? ?? {};
    
    setState(() => _isExporting = true);
    
    try {
      final pdfBytes = await _pdfService.generateReport(
        cropName: args['crop']?.toString() ?? '',
        diseaseName: args['disease']?.toString() ?? '',
        confidence: (args['confidence'] as num?)?.toDouble() ?? 0.0,
        pesticide: args['pesticide']?.toString() ?? '',
        dosage: args['dosage']?.toString() ?? '',
        instructions: args['instructions']?.toString() ?? '',
        imageFile: predictionProvider.selectedImage,
      );
      
      await _pdfService.shareReport(pdfBytes, 'pesticare_report.pdf');
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Failed to generate PDF')),
        );
      }
    } finally {
      if (mounted) setState(() => _isExporting = false);
    }
  }

  Future<void> _shareResults() async {
    final args = ModalRoute.of(context)?.settings.arguments as Map<String, dynamic>? ?? {};
    
    final message = '''
🌿 *Pesti-Care Diagnosis Report*

🌾 *Crop:* ${args['crop'] ?? 'N/A'}
🔬 *Disease:* ${args['disease'] ?? 'N/A'}
📊 *Confidence:* ${((args['confidence'] as num?)?.toDouble() ?? 0) * 100}%

💊 *Recommended Pesticide:* ${args['pesticide'] ?? 'N/A'}
📏 *Dosage:* ${args['dosage'] ?? 'N/A'}

_Generated by Pesti-Care App_
''';

    await Share.share(message, subject: 'Pesti-Care Diagnosis');
  }

  @override
  Widget build(BuildContext context) {
    final localization = context.watch<LanguageProvider>();
    final predictionProvider = context.watch<PredictionProvider>();
    final theme = Theme.of(context);
    
    final args =
        ModalRoute.of(context)?.settings.arguments as Map<String, dynamic>? ??
            {};
    final cropKey = args['cropKey'] as String? ?? '';
    final disease = args['disease']?.toString() ?? 'N/A';
    final pesticide = args['pesticide']?.toString() ?? 'N/A';
    final dosage = args['dosage']?.toString() ?? 'N/A';
    final instructions = args['instructions']?.toString() ?? '';
    final confidence = args['confidence'] is num
        ? (args['confidence'] as num).toDouble()
        : 0.0;
    
    final isLowConfidence = confidence < predictionProvider.confidenceThreshold;
    final selectedImage = predictionProvider.selectedImage;
    final diseaseInfo = DiseaseDatabase.getInfo(disease);

    return Scaffold(
      appBar: AppBar(
        title: Text(localization.translate('analyzeDisease')),
        actions: [
          // Share button
          IconButton(
            icon: const Icon(Icons.share),
            onPressed: _shareResults,
            tooltip: localization.translate('share'),
          ),
          // PDF export button
          IconButton(
            icon: _isExporting 
                ? const SizedBox(
                    width: 20,
                    height: 20,
                    child: CircularProgressIndicator(strokeWidth: 2),
                  )
                : const Icon(Icons.picture_as_pdf),
            onPressed: _isExporting ? null : _exportPdf,
            tooltip: localization.translate('exportPdf'),
          ),
          const ThemeToggleButton(),
          const LanguageActionButton(),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(20),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Scanned image preview
            if (selectedImage != null)
              Container(
                width: double.infinity,
                height: 180,
                margin: const EdgeInsets.only(bottom: 16),
                decoration: BoxDecoration(
                  borderRadius: BorderRadius.circular(16),
                  boxShadow: [
                    BoxShadow(
                      color: theme.colorScheme.shadow.withValues(alpha: 0.1),
                      blurRadius: 10,
                      offset: const Offset(0, 4),
                    ),
                  ],
                ),
                clipBehavior: Clip.antiAlias,
                child: Image.file(
                  selectedImage,
                  fit: BoxFit.cover,
                ),
              ),
            
            // Low confidence warning banner
            if (isLowConfidence) ...[
              Container(
                width: double.infinity,
                padding: const EdgeInsets.all(16),
                margin: const EdgeInsets.only(bottom: 16),
                decoration: BoxDecoration(
                  color: Colors.orange.shade100,
                  borderRadius: BorderRadius.circular(12),
                  border: Border.all(color: Colors.orange.shade400, width: 1.5),
                ),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Row(
                      children: [
                        Icon(Icons.warning_amber_rounded, 
                            color: Colors.orange.shade700, size: 24),
                        const SizedBox(width: 8),
                        Expanded(
                          child: Text(
                            localization.translate('lowConfidenceWarning'),
                            style: TextStyle(
                              fontWeight: FontWeight.bold,
                              color: Colors.orange.shade900,
                              fontSize: 16,
                            ),
                          ),
                        ),
                      ],
                    ),
                    const SizedBox(height: 8),
                    Text(
                      'Confidence is ${(confidence * 100).toStringAsFixed(1)}%. Consider uploading a clearer image.',
                      style: TextStyle(
                        color: Colors.orange.shade800,
                        fontSize: 14,
                      ),
                    ),
                    const SizedBox(height: 12),
                    SizedBox(
                      width: double.infinity,
                      child: ElevatedButton.icon(
                        onPressed: () => Navigator.pop(context),
                        icon: const Icon(Icons.camera_alt),
                        label: Text(localization.translate('reuploadImage')),
                        style: ElevatedButton.styleFrom(
                          backgroundColor: Colors.orange.shade600,
                          foregroundColor: Colors.white,
                        ),
                      ),
                    ),
                  ],
                ),
              ),
            ],
            
            // Result tiles
            _ResultInfoTile(
              label: localization.translate('selectedCrop'),
              value: localization.translate(cropKey),
              icon: Icons.grass,
            ),
            _ResultInfoTile(
              label: localization.translate('diseaseName'),
              value: disease,
              icon: Icons.bug_report,
              isHighlighted: true,
            ),
            _ResultInfoTile(
              label: localization.translate('confidence'),
              value: '${(confidence * 100).toStringAsFixed(1)}%',
              icon: Icons.analytics,
              isWarning: isLowConfidence,
            ),
            _ResultInfoTile(
              label: localization.translate('recommendedPesticide'),
              value: pesticide,
              icon: Icons.science,
            ),
            _ResultInfoTile(
              label: localization.translate('dosage'),
              value: dosage,
              icon: Icons.medication,
            ),
            if (instructions.isNotEmpty)
              _PesticideAccordion(
                instructions: instructions,
                localization: localization,
              ),
            
            // Disease info expandable card
            if (diseaseInfo != null) ...[
              const SizedBox(height: 8),
              _DiseaseInfoCard(
                diseaseInfo: diseaseInfo,
                isExpanded: _diseaseInfoExpanded,
                onToggle: () => setState(() => _diseaseInfoExpanded = !_diseaseInfoExpanded),
                localization: localization,
              ),
            ],
            
            const SizedBox(height: 24),
            
            // Action buttons
            Row(
              children: [
                Expanded(
                  child: OutlinedButton.icon(
                    onPressed: _shareResults,
                    icon: const Icon(Icons.share),
                    label: Text(localization.translate('share')),
                  ),
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: ElevatedButton.icon(
                    onPressed: () => _scanAnother(context),
                    icon: const Icon(Icons.qr_code_scanner),
                    label: Text(localization.translate('scanAnother')),
                  ),
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }

  void _scanAnother(BuildContext context) {
    context.read<PredictionProvider>().reset();
    Navigator.pushNamedAndRemoveUntil(
      context,
      HomeScreen.routeName,
      (route) => false,
    );
  }
}

class _ResultInfoTile extends StatelessWidget {
  const _ResultInfoTile({
    required this.label,
    required this.value,
    this.icon,
    this.isWarning = false,
    this.isHighlighted = false,
  });

  final String label;
  final String value;
  final IconData? icon;
  final bool isWarning;
  final bool isHighlighted;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    
    Color? backgroundColor;
    Color? borderColor;
    Color? iconColor;
    
    if (isWarning) {
      backgroundColor = Colors.orange.shade50;
      borderColor = Colors.orange.shade300;
      iconColor = Colors.orange.shade600;
    } else if (isHighlighted) {
      backgroundColor = theme.colorScheme.primaryContainer.withValues(alpha: 0.3);
      borderColor = theme.colorScheme.primary.withValues(alpha: 0.3);
      iconColor = theme.colorScheme.primary;
    }
    
    return Container(
      width: double.infinity,
      margin: const EdgeInsets.only(bottom: 10),
      padding: const EdgeInsets.all(14),
      decoration: BoxDecoration(
        color: backgroundColor ??
            theme.colorScheme.surfaceContainerHighest.withValues(alpha: 0.6),
        borderRadius: BorderRadius.circular(14),
        border: borderColor != null 
            ? Border.all(color: borderColor, width: 1.5)
            : null,
      ),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          if (icon != null) ...[
            Icon(
              icon,
              size: 18,
              color: iconColor ?? theme.colorScheme.primary.withValues(alpha: 0.7),
            ),
            const SizedBox(width: 10),
          ],
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  label,
                  style: theme.textTheme.labelSmall?.copyWith(
                    color: isWarning 
                        ? Colors.orange.shade700
                        : theme.colorScheme.onSurface.withValues(alpha: 0.6),
                  ),
                ),
                const SizedBox(height: 2),
                Text(
                  value,
                  style: theme.textTheme.bodyLarge?.copyWith(
                    fontWeight: isHighlighted ? FontWeight.bold : FontWeight.w500,
                    color: isWarning ? Colors.orange.shade800 : null,
                  ),
                ),
              ],
            ),
          ),
          if (isWarning)
            Icon(Icons.warning_amber, color: Colors.orange.shade600, size: 18),
        ],
      ),
    );
  }
}

class _DiseaseInfoCard extends StatelessWidget {
  const _DiseaseInfoCard({
    required this.diseaseInfo,
    required this.isExpanded,
    required this.onToggle,
    required this.localization,
  });

  final DiseaseInfo diseaseInfo;
  final bool isExpanded;
  final VoidCallback onToggle;
  final LanguageProvider localization;

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    
    return AnimatedContainer(
      duration: const Duration(milliseconds: 300),
      curve: Curves.easeInOut,
      decoration: BoxDecoration(
        color: theme.colorScheme.tertiaryContainer.withValues(alpha: 0.3),
        borderRadius: BorderRadius.circular(16),
        border: Border.all(
          color: theme.colorScheme.tertiary.withValues(alpha: 0.3),
        ),
      ),
      clipBehavior: Clip.antiAlias,
      child: Column(
        children: [
          InkWell(
            onTap: onToggle,
            child: Padding(
              padding: const EdgeInsets.all(16),
              child: Row(
                children: [
                  Icon(
                    Icons.lightbulb_outline,
                    color: theme.colorScheme.tertiary,
                    size: 22,
                  ),
                  const SizedBox(width: 12),
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          localization.translate('diseaseInfo'),
                          style: theme.textTheme.titleSmall?.copyWith(
                            fontWeight: FontWeight.bold,
                            color: theme.colorScheme.onTertiaryContainer,
                          ),
                        ),
                        Text(
                          isExpanded ? localization.translate('tapToHide') : localization.translate('tapToView'),
                          style: theme.textTheme.bodySmall?.copyWith(
                            color: theme.colorScheme.onTertiaryContainer.withValues(alpha: 0.7),
                          ),
                        ),
                      ],
                    ),
                  ),
                  AnimatedRotation(
                    turns: isExpanded ? 0.5 : 0,
                    duration: const Duration(milliseconds: 300),
                    child: Icon(
                      Icons.expand_more,
                      color: theme.colorScheme.tertiary,
                    ),
                  ),
                ],
              ),
            ),
          ),
          AnimatedCrossFade(
            firstChild: const SizedBox.shrink(),
            secondChild: Padding(
              padding: const EdgeInsets.fromLTRB(16, 0, 16, 16),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  const Divider(),
                  const SizedBox(height: 8),
                  Text(
                    diseaseInfo.description,
                    style: theme.textTheme.bodyMedium,
                  ),
                  if (diseaseInfo.causes.isNotEmpty) ...[
                    const SizedBox(height: 16),
                    _buildSection(theme, localization.translate('causes'), Icons.help_outline, diseaseInfo.causes),
                  ],
                  if (diseaseInfo.symptoms.isNotEmpty) ...[
                    const SizedBox(height: 12),
                    _buildSection(theme, localization.translate('symptoms'), Icons.visibility, diseaseInfo.symptoms),
                  ],
                  if (diseaseInfo.prevention.isNotEmpty) ...[
                    const SizedBox(height: 12),
                    _buildSection(theme, localization.translate('prevention'), Icons.shield_outlined, diseaseInfo.prevention),
                  ],
                ],
              ),
            ),
            crossFadeState: isExpanded ? CrossFadeState.showSecond : CrossFadeState.showFirst,
            duration: const Duration(milliseconds: 300),
          ),
        ],
      ),
    );
  }

  Widget _buildSection(ThemeData theme, String title, IconData icon, List<String> items) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            Icon(icon, size: 16, color: theme.colorScheme.tertiary),
            const SizedBox(width: 6),
            Text(
              title,
              style: theme.textTheme.labelMedium?.copyWith(
                fontWeight: FontWeight.bold,
                color: theme.colorScheme.tertiary,
              ),
            ),
          ],
        ),
        const SizedBox(height: 6),
        ...items.map((item) => Padding(
          padding: const EdgeInsets.only(left: 22, bottom: 4),
          child: Row(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text('• ', style: TextStyle(color: theme.colorScheme.tertiary)),
              Expanded(
                child: Text(
                  item,
                  style: theme.textTheme.bodySmall,
                ),
              ),
            ],
          ),
        )),
      ],
    );
  }
}

class _PesticideAccordion extends StatefulWidget {
  const _PesticideAccordion({
    required this.instructions,
    required this.localization,
  });

  final String instructions;
  final LanguageProvider localization;

  @override
  State<_PesticideAccordion> createState() => _PesticideAccordionState();
}

class _PesticideAccordionState extends State<_PesticideAccordion> {
  bool _isExpanded = true; 

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    
    return AnimatedContainer(
      duration: const Duration(milliseconds: 300),
      curve: Curves.easeInOut,
      margin: const EdgeInsets.only(bottom: 10),
      decoration: BoxDecoration(
        color: theme.colorScheme.secondaryContainer.withValues(alpha: 0.4),
        borderRadius: BorderRadius.circular(16),
        border: Border.all(
          color: theme.colorScheme.secondary.withValues(alpha: 0.3),
        ),
      ),
      clipBehavior: Clip.antiAlias,
      child: Column(
        children: [
          InkWell(
            onTap: () => setState(() => _isExpanded = !_isExpanded),
            child: Padding(
              padding: const EdgeInsets.all(16),
              child: Row(
                children: [
                  Icon(
                    Icons.format_list_numbered,
                    color: theme.colorScheme.secondary,
                    size: 22,
                  ),
                  const SizedBox(width: 12),
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          widget.localization.translate('instructions') ?? 'Action Plan',
                          style: theme.textTheme.titleSmall?.copyWith(
                            fontWeight: FontWeight.bold,
                            color: theme.colorScheme.onSecondaryContainer,
                          ),
                        ),
                        Text(
                          _isExpanded 
                              ? widget.localization.translate('tapToHide') 
                              : widget.localization.translate('tapToView'),
                          style: theme.textTheme.bodySmall?.copyWith(
                            color: theme.colorScheme.onSecondaryContainer.withValues(alpha: 0.7),
                          ),
                        ),
                      ],
                    ),
                  ),
                  AnimatedRotation(
                    turns: _isExpanded ? 0.5 : 0,
                    duration: const Duration(milliseconds: 300),
                    child: Icon(
                      Icons.expand_more,
                      color: theme.colorScheme.secondary,
                    ),
                  ),
                ],
              ),
            ),
          ),
          AnimatedCrossFade(
            firstChild: const SizedBox.shrink(),
            secondChild: Padding(
              padding: const EdgeInsets.fromLTRB(16, 0, 16, 16),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  const Divider(),
                  const SizedBox(height: 8),
                  Text(
                    widget.instructions,
                    style: theme.textTheme.bodyMedium,
                  ),
                ],
              ),
            ),
            crossFadeState: _isExpanded ? CrossFadeState.showSecond : CrossFadeState.showFirst,
            duration: const Duration(milliseconds: 300),
          ),
        ],
      ),
    );
  }
}
